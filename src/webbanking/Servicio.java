package webbanking;

/**
 *
 * @author sotelo
 */
public class Servicio {
    private String nombreServicio;
    private String proveedorServicio;
    private String detalleServicio;

    public Servicio(String nombreServicio, String proveedorServicio, String detalleServicio) {
        this.nombreServicio = nombreServicio;
        this.proveedorServicio = proveedorServicio;
        this.detalleServicio = detalleServicio;
    }

    public String getNombreServicio() {
        return nombreServicio;
    }

    public void setNombreServicio(String nombreServicio) {
        this.nombreServicio = nombreServicio;
    }

    public String getProveedorServicio() {
        return proveedorServicio;
    }

    public void setProveedorServicio(String proveedorServicio) {
        this.proveedorServicio = proveedorServicio;
    }

    public String getDetalleServicio() {
        return detalleServicio;
    }

    public void setDetalleServicio(String detalleServicio) {
        this.detalleServicio = detalleServicio;
    }
    
    
}
